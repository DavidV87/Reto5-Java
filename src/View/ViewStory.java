    private void jBCShareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCShareActionPerformed
        if(controller.validateEmptyField(jTFCTitle.getText(),jTFCAuthor.getText(),jCBCType.getSelectedIndex(), jTPCStory.getText(),jTACDescription.getText())){
            controller.setUserId(controller.getUserId());
            if(controller.createUserStory()){
                jTFCTitle.setText("");
                jTFCAuthor.setText("");
                jTACDescription.setText("");
                jTPCStory.setText("");
                jCBCType.removeAllItems();
                jCBCType.addItem("");
                jTFCAuthor.setText(controller.getName()+" "+controller.getLastName());
            if (logged){
                if (controller.loadStoryTypes()){
                    for(String type : controller.getStoryTypes()){
                        jCBCType.addItem(type);
                    }
                }
                if (controller.createQueryStory("",0,"","",controller.getId())){
                    if(controller.getStories().isEmpty()){
                        jLCStories.setVisible(false);
                    }
                    else{
                        jLCStories.setVisible(true);
                    }
                    jLCSStories.setEnabled(true);
                    DefaultListModel model = new DefaultListModel();
                    for (int i = 0; i < controller.getStories().size();i++){
                        model.addElement(controller.getStories().get(i).getTitle());
                    }
                    jLCStories.setModel(model);
                }
                
                
            }
                JOptionPane.showMessageDialog(this,"Se compartio tu historia con exito");
            }else{
                JOptionPane.showMessageDialog(this,"No pude compartir tu historia");
            }
        }else{
            JOptionPane.showMessageDialog(this,"Debes llenar todos los campos");
        }
    }//GEN-LAST:event_jBCShareActionPerformed

