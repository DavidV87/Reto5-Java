    private void jTPanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTPanelMouseClicked
        clearFieldsSearchPanel();
        if(jTPanel.getSelectedIndex()==1){
            jCBType.removeAllItems();
            jCBTitle.removeAllItems();
            jCBAuthor.removeAllItems();
            jCBType.addItem("");
            jCBTitle.addItem("");
            jCBAuthor.addItem("");
            if (controller.loadStoryTypes()){
                for(String story : controller.getStoryTypes()){
                    jCBType.addItem(story);
                }
            } 
            if (controller.loadStoryAutor()){
                for (String author: controller.getStoryAutor()){
                    jCBAuthor.addItem(author);
                }
            }
            if (controller.loadStoryTitle()){
                for (String title: controller.getStoryTitle()){
                    jCBTitle.addItem(title);
                }
            }
        }
        if(jTPanel.getSelectedIndex()==2){
            jCBCType.removeAllItems();
            jCBCType.addItem("");
            if (logged){
                if (controller.loadStoryTypes()){
                    for(String type : controller.getStoryTypes()){
                        jCBCType.addItem(type);
                    }
                }
                if (controller.createQueryStory("",0,"","",controller.getId())){
                    if(controller.getStories().isEmpty()){
                        jLCStories.setVisible(false);
                    }
                    else{
                        jLCStories.setVisible(true);
                    }
                    jLCSStories.setEnabled(true);
                    DefaultListModel model = new DefaultListModel();
                    for (int i = 0; i < controller.getStories().size();i++){
                        model.addElement(controller.getStories().get(i).getTitle());
                    }
                    jLCStories.setModel(model);
                }
                
                
            }
        }
    }//GEN-LAST:event_jTPanelMouseClicked

